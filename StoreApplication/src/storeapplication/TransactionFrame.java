    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
        String Hub = ComboFood.getSelectedItem().toString();
        int qty = Integer.parseInt(Qty.getText());
        double price = 0.0;
        
        switch (Hub){
            case "Fries": price = 59.00; break;
            case "Mango Shake": price = 50.00; break;
            case "Sisig": price = 99.00; break;
            case "Burger Steak": price = 139.00; break;
            case "Burger Plus": price = 120.00; break;
        }
        
       double totalPrice = qty * price;
        
       total+=totalPrice;
               
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{Hub,qty,price,Total});
        JOptionPane.showMessageDialog(this, "Price of the selected item: " + totalPrice);
    }//GEN-LAST:event_AddActionPerformed

    
